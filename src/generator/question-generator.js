    /**
     * Generate follow-up questions based on previous answers
     */
    async generateFollowUpQuestions(context, previousAnswers) {
        const followUpQuestions = [];

        // Analyze previous answers to determine what needs clarification
        for (const [questionId, answer] of Object.entries(previousAnswers)) {
            const question = context.questions.find(q => q.id === questionId);
            if (!question) continue;

            // Generate follow-ups based on specific answers
            if (question.category === 'User & Audience' && answer.toLowerCase().includes('multiple')) {
                followUpQuestions.push({
                    id: `followup_${questionId}_1`,
                    category: 'User & Audience',
                    text: 'What are the different user roles and their permissions?',
                    priority: 1,
                    dependsOn: [questionId]
                });
            }

            if (question.category === 'Core Functionality' && answer.toLowerCase().includes('payment')) {
                followUpQuestions.push({
                    id: `followup_${questionId}_2`,
                    category: 'Core Functionality',
                    text: 'What payment methods should be supported?',
                    priority: 1,
                    dependsOn: [questionId]
                });
            }

            if (question.category === 'Access & Deployment' && answer.toLowerCase().includes('mobile')) {
                followUpQuestions.push({
                    id: `followup_${questionId}_3`,
                    category: 'Access & Deployment',
                    text: 'Should the mobile app support both iOS and Android?',
                    priority: 1,
                    dependsOn: [questionId]
                });
            }
        }

        return followUpQuestions;
    }

    /**
     * Check if we have enough information
     */
    isContextComplete(context) {
        const answeredCount = context.answers.size;
        const totalQuestions = context.questions.length;

        // Context is complete if:
        // 1. We have at least 3 answers
        // 2. We've answered at least 50% of questions
        // 3. All priority 1 questions are answered

        if (answeredCount < 3) {
            return false;
        }

        const priority1Questions = context.questions.filter(q => q.priority === 1);
        const answeredPriority1 = priority1Questions.filter(q => context.answers.has(q.id));

        if (answeredPriority1.length < priority1Questions.length) {
            return false;
        }

        const completionRate = answeredCount / totalQuestions;
        return completionRate >= 0.5;
    }

    /**
     * Get next question to ask
     */
    getNextQuestion(questions, answeredQuestions) {
        // Filter out already answered questions
        const unanswered = questions.filter(q => !answeredQuestions.has(q.id));

        if (unanswered.length === 0) {
            return null;
        }

        // Check dependencies
        const available = unanswered.filter(q => {
            if (!q.dependsOn || q.dependsOn.length === 0) {
                return true;
            }
            return q.dependsOn.every(depId => answeredQuestions.has(depId));
        });

        if (available.length === 0) {
            return unanswered[0]; // Return first unanswered if no available
        }

        // Return highest priority available question
        available.sort((a, b) => a.priority - b.priority);
        return available[0];
    }

    /**
     * Detect if a question should allow multiple selections
     */
    detectAllowMultiple(questionText) {
        const text = questionText.toLowerCase();

        // Keywords that suggest multiple selection
        const multipleKeywords = [
            'which', 'what', 'select', 'choose',
            'features', 'platforms', 'users', 'types',
            'methods', 'systems', 'integrations',
            'requirements', 'constraints', 'criteria'
        ];

        // Phrases that suggest multiple selection
        const multiplePhrases = [
            'top 3', 'top three', 'top 5', 'top five',
            'most important', 'critical features',
            'key features', 'main features',
            'which of', 'what are', 'list',
            'multiple', 'several', 'various'
        ];

        // Check for multiple indicators
        const hasMultipleKeyword = multipleKeywords.some(keyword => text.includes(keyword));
        const hasMultiplePhrase = multiplePhrases.some(phrase => text.includes(phrase));

        // Questions asking for specific numbers (top 3, etc.) should allow multiple
        const hasNumberRequest = /top \d+|top (three|five|ten)/i.test(text);

        return hasMultipleKeyword && (hasMultiplePhrase || hasNumberRequest);
    }

    /**
     * Generate follow-up questions based on answers (for deeper context)
     */
    async generateFollowUpRound(context, previousAnswers, roundNumber) {
        logger.progress(`\nLet me think for a while...`);

        try {
            // Analyze previous answers to find areas needing clarification
            const areasNeedingClarification = this.identifyUnclearAreas(previousAnswers);

            // if (areasNeedingClarification.length === 0) {
            //     logger.info('No additional clarification needed.');
            //     return [];
            // }

            // Generate targeted follow-up questions
            const followUpQuestionsResponse = await this.watsonxClient.generateQuestions({
                goal: context.goal,
                analysis: context.analysis,
                previousAnswers: previousAnswers,
                areasToClarity: areasNeedingClarification,
                roundNumber: roundNumber,
                maxQuestions: Math.min(5, this.maxQuestions)
            });

            const followUpQuestions = followUpQuestionsResponse.questions;

            logger.debug(followUpQuestionsResponse);

            if (followUpQuestions && followUpQuestions.length > 0) {
                return { ...followUpQuestionsResponse, questions: followUpQuestions.map(x => ({ ...x, id: `follow-up-${roundNumber}-${x.id}` })) }; // Return only the first 5 questions
            }
        } catch (error) {
            logger.warn('Failed to generate follow-up questions:', error.message);
            logger.debug(error);
        } finally {
            logger.progressDone();
        }

        return [];
    }

    /**
     * Identify areas that need more clarification
     */
    identifyUnclearAreas(answers) {
        const unclear = [];

        if (Array.isArray(answers)) {
            for (const qna of answers) {
                const { answer, question } = qna;
                const answerText = answer.toLowerCase();
                const questionText = question.toLowerCase();
                // if answer is not provided or vague
                if (!answerText || answerText.includes('not sure') || answerText.includes('maybe') || answerText.includes('not specified') || answerText.length < 10) {
                    unclear.push(question);
                }
            }
        }
        else {
            // Lagecy format
            for (const [questionId, answer] of Object.entries(answers)) {
                const answerText = answer.toLowerCase();

                // Check for vague answers
                if (answerText.includes('not sure') ||
                    answerText.includes('maybe') ||
                    answerText.includes('not specified') ||
                    answerText.length < 10) {
                    unclear.push(questionId);
                }
            }
        }

        return unclear;
    }

    /**
     * Get question statistics
     */
    getStatistics(questions, answeredQuestions) {
        const total = questions.length;
        const answered = answeredQuestions.size;
        const remaining = total - answered;

        const byCategory = {};
        questions.forEach(q => {
            if (!byCategory[q.category]) {
                byCategory[q.category] = { total: 0, answered: 0 };
            }
            byCategory[q.category].total++;
            if (answeredQuestions.has(q.id)) {
                byCategory[q.category].answered++;
            }
        });

        return {
            total,
            answered,
            remaining,
            completionRate: total > 0 ? (answered / total * 100).toFixed(0) : 0,
            byCategory
        };
    }
