/**
 * Question Generator
 * 
 * Generates relevant clarifying questions based on goal analysis
 * and adapts based on previous answers.
 */

const { getLogger } = require('../utils/logger');

const logger = getLogger();

class QuestionGenerator {
    constructor(watsonxClient, config) {
        this.watsonxClient = watsonxClient;
        this.config = config;
        this.maxQuestions = config.prompts.maxQuestions || 10;
    }

    /**
     * Generate initial questions based on analysis
     */
    async generateInitialQuestions(analysis) {
        logger.section('Generating Questions');
        logger.info(`Generating up to ${this.maxQuestions} clarifying questions...`);

        let questions = [];

        // Try to generate questions using AI
        try {
            logger.progress('Using AI to generate questions');
            const aiQuestions = await this.watsonxClient.generateQuestions({
                goal: analysis.goal,
                analysis: analysis,
                maxQuestions: this.maxQuestions
            });

            if (aiQuestions && aiQuestions.length > 0) {
                questions = aiQuestions;
                logger.progressDone();
            } else {
                throw new Error('No questions generated by AI');
            }
        } catch (error) {
            logger.warn('AI question generation failed, using template questions');
            questions = this.generateTemplateQuestions(analysis);
        }

        // Ensure questions have proper structure
        questions = questions.map((q, index) => ({
            id: q.id || `q${index + 1}`,
            category: q.category || 'General',
            text: q.text,
            priority: q.priority || 2,
            allowMultiple: q.allowMultiple || false,
            dependsOn: q.dependsOn || [],
            suggestedAnswers: q.suggestedAnswers || []
        }));

        // Limit to max questions
        questions = questions.slice(0, this.maxQuestions);

        // Sort by priority
        questions.sort((a, b) => a.priority - b.priority);

        logger.success(`Generated ${questions.length} questions`);
        return questions;
    }

    /**
     * Generate template questions based on analysis
     */
    generateTemplateQuestions(analysis) {
        const questions = [];
        let idCounter = 1;

        // User & Audience questions
        if (!analysis.extractedInfo.users || analysis.extractedInfo.users.length === 0) {
            questions.push({
                id: `q${idCounter++}`,
                category: 'User & Audience',
                text: 'Who are the primary users of this application?',
                priority: 1,
                allowMultiple: true,
                suggestedAnswers: ['General public', 'Business users', 'Internal team', 'Specific professionals']
            });
        }

        questions.push({
            id: `q${idCounter++}`,
            category: 'User & Audience',
            text: 'What is the expected number of users?',
            priority: 2,
            allowMultiple: false,
            suggestedAnswers: ['< 100', '100-1,000', '1,000-10,000', '> 10,000']
        });

        // Access & Deployment questions
        questions.push({
            id: `q${idCounter++}`,
            category: 'Access & Deployment',
            text: 'How will users access this application?',
            priority: 1,
            allowMultiple: true,
            suggestedAnswers: ['Web browser', 'Mobile app (iOS/Android)', 'Desktop application', 'API only']
        });

        questions.push({
            id: `q${idCounter++}`,
            category: 'Access & Deployment',
            text: 'What is the preferred deployment model?',
            priority: 2,
            allowMultiple: false,
            suggestedAnswers: ['Cloud (AWS/Azure/GCP)', 'On-premises', 'Hybrid', 'Not sure']
        });

        // Core Functionality questions
        questions.push({
            id: `q${idCounter++}`,
            category: 'Core Functionality',
            text: 'What are the top 3 most important features?',
            priority: 1,
            allowMultiple: false,
            suggestedAnswers: []
        });

        questions.push({
            id: `q${idCounter++}`,
            category: 'Core Functionality',
            text: 'Are there any existing systems this needs to integrate with?',
            priority: 2,
            allowMultiple: true,
            suggestedAnswers: ['No integrations needed', 'Database systems', 'Third-party APIs', 'Legacy systems']
        });

        // Success Criteria questions
        questions.push({
            id: `q${idCounter++}`,
            category: 'Success Criteria',
            text: 'How will you measure the success of this application?',
            priority: 1,
            allowMultiple: true,
            suggestedAnswers: ['User adoption rate', 'Performance metrics', 'Business KPIs', 'User satisfaction']
        });

        // Constraints questions
        questions.push({
            id: `q${idCounter++}`,
            category: 'Constraints',
            text: 'Are there any specific technical constraints or requirements?',
            priority: 2,
            allowMultiple: true,
            suggestedAnswers: ['Must use specific technology', 'Performance requirements', 'Security requirements', 'No specific constraints']
        });

        // Priority questions
        questions.push({
            id: `q${idCounter++}`,
            category: 'Priority',
            text: 'What is the target timeline for the first version?',
            priority: 2,
            allowMultiple: false,
            suggestedAnswers: ['< 1 month', '1-3 months', '3-6 months', '> 6 months']
        });

        questions.push({
            id: `q${idCounter++}`,
            category: 'Priority',
            text: 'What is more important: speed to market or feature completeness?',
            priority: 2,
            allowMultiple: false,
            suggestedAnswers: ['Speed to market (MVP approach)', 'Feature completeness', 'Balanced approach']
        });

        return questions;
    }

    /**
     * Generate follow-up questions based on previous answers
     */
    async generateFollowUpQuestions(context, previousAnswers) {
        const followUpQuestions = [];

        // Analyze previous answers to determine what needs clarification
        for (const [questionId, answer] of Object.entries(previousAnswers)) {
            const question = context.questions.find(q => q.id === questionId);
            if (!question) continue;

            // Generate follow-ups based on specific answers
            if (question.category === 'User & Audience' && answer.toLowerCase().includes('multiple')) {
                followUpQuestions.push({
                    id: `followup_${questionId}_1`,
                    category: 'User & Audience',
                    text: 'What are the different user roles and their permissions?',
                    priority: 1,
                    dependsOn: [questionId]
                });
            }

            if (question.category === 'Core Functionality' && answer.toLowerCase().includes('payment')) {
                followUpQuestions.push({
                    id: `followup_${questionId}_2`,
                    category: 'Core Functionality',
                    text: 'What payment methods should be supported?',
                    priority: 1,
                    dependsOn: [questionId]
                });
            }

            if (question.category === 'Access & Deployment' && answer.toLowerCase().includes('mobile')) {
                followUpQuestions.push({
                    id: `followup_${questionId}_3`,
                    category: 'Access & Deployment',
                    text: 'Should the mobile app support both iOS and Android?',
                    priority: 1,
                    dependsOn: [questionId]
                });
            }
        }

        return followUpQuestions;
    }

    /**
     * Check if we have enough information
     */
    isContextComplete(context) {
        const answeredCount = context.answers.size;
        const totalQuestions = context.questions.length;

        // Context is complete if:
        // 1. We have at least 3 answers
        // 2. We've answered at least 50% of questions
        // 3. All priority 1 questions are answered

        if (answeredCount < 3) {
            return false;
        }

        const priority1Questions = context.questions.filter(q => q.priority === 1);
        const answeredPriority1 = priority1Questions.filter(q => context.answers.has(q.id));

        if (answeredPriority1.length < priority1Questions.length) {
            return false;
        }

        const completionRate = answeredCount / totalQuestions;
        return completionRate >= 0.5;
    }

    /**
     * Get next question to ask
     */
    getNextQuestion(questions, answeredQuestions) {
        // Filter out already answered questions
        const unanswered = questions.filter(q => !answeredQuestions.has(q.id));

        if (unanswered.length === 0) {
            return null;
        }

        // Check dependencies
        const available = unanswered.filter(q => {
            if (!q.dependsOn || q.dependsOn.length === 0) {
                return true;
            }
            return q.dependsOn.every(depId => answeredQuestions.has(depId));
        });

        if (available.length === 0) {
            return unanswered[0]; // Return first unanswered if no available
        }

        // Return highest priority available question
        available.sort((a, b) => a.priority - b.priority);
        return available[0];
    }

    /**
     * Get question statistics
     */
    getStatistics(questions, answeredQuestions) {
        const total = questions.length;
        const answered = answeredQuestions.size;
        const remaining = total - answered;

        const byCategory = {};
        questions.forEach(q => {
            if (!byCategory[q.category]) {
                byCategory[q.category] = { total: 0, answered: 0 };
            }
            byCategory[q.category].total++;
            if (answeredQuestions.has(q.id)) {
                byCategory[q.category].answered++;
            }
        });

        return {
            total,
            answered,
            remaining,
            completionRate: total > 0 ? (answered / total * 100).toFixed(0) : 0,
            byCategory
        };
    }
}

module.exports = QuestionGenerator;

