/**
 * Markdown Formatter
 * 
 * Formats specification content as markdown with consistent styling.
 */

const { sanitizeMarkdown } = require('../utils/validators');
const { getLogger } = require('../utils/logger');

const logger = getLogger();

class MarkdownFormatter {
    constructor(config) {
        this.config = config;
    }

    /**
     * Format complete specification
     */
    format(spec, context) {
        logger.debug('Formatting specification as markdown');

        const parts = [];

        // Add metadata header
        parts.push(this.addMetadata(spec, context));

        // Add table of contents
        parts.push(this.generateTOC(spec));

        // Add all sections
        for (const [sectionName, content] of Object.entries(spec.sections)) {
            parts.push(this.formatSection(sectionName, content));
        }

        // Add footer
        parts.push(this.addFooter(context));

        return parts.join('\n\n');
    }

    /**
     * Format Q&A
     */
    formatQna(context) {
        logger.debug('Formatting Q&A as markdown');
        let formatted = ""
        for (const qna of context) {
            let { question, answer } = qna;
            logger.debug(qna);
            formatted += `### Question: ${question}\n\n    Answer:${answer}\n\n`;
        }
        return formatted;
    }

    /**
     * Format individual section
     */
    formatSection(sectionName, content) {
        const sanitized = sanitizeMarkdown(content);

        // Ensure section starts with proper heading
        if (!sanitized.startsWith('#')) {
            return `## ${sectionName}\n\n${sanitized}`;
        }

        return sanitized;
    }

    /**
     * Generate table of contents
     */
    generateTOC(spec) {
        const toc = ['## Table of Contents\n'];

        let index = 1;
        for (const sectionName of Object.keys(spec.sections)) {
            const anchor = sectionName.toLowerCase().replace(/\s+/g, '-').replace(/[^\w-]/g, '');
            toc.push(`${index}. [${sectionName}](#${anchor})`);
            index++;
        }

        return toc.join('\n');
    }

    /**
     * Add metadata header
     */
    addMetadata(spec, context) {
        const timestamp = this.config.output.includeTimestamp
            ? new Date().toISOString().split('T')[0]
            : '';

        const lines = [
            '# Software Specification',
            '',
            '> Generated by SpecBaker 🎂',
            '> Powered by IBM watsonx.ai',
            ''
        ];

        if (timestamp) {
            lines.push(`**Generated:** ${timestamp}`, '');
        }

        if (context.goal) {
            lines.push('**Original Goal:**', '', `> ${context.goal}`, '');
        }

        if (context.analysis) {
            lines.push('**Project Complexity:** ' + this.capitalizeFirst(context.analysis.complexity));
            lines.push('**Domain:** ' + this.capitalizeFirst(context.analysis.domain));
            lines.push('');
        }

        lines.push('---', '');

        return lines.join('\n');
    }

    /**
     * Add footer
     */
    addFooter(context) {
        const lines = [
            '---',
            '',
            '## Document Information',
            '',
            '**Generated by:** SpecBaker',
            `**Version:** ${context.metadata?.version || '1.0.0'}`,
            `**Generated at:** ${new Date().toISOString()}`,
            '',
            '### How to Use This Specification',
            '',
            '1. **Review** - Read through all sections carefully',
            '2. **Clarify** - Ask questions about anything unclear',
            '3. **Implement** - Use the "Bob-Ready Prompt" section to start development',
            '4. **Iterate** - Update this spec as requirements evolve',
            '',
            '### Next Steps',
            '',
            '- Share this specification with your development team',
            '- Use the Bob-Ready Prompt with IBM Bob for implementation',
            '- Set up your development environment',
            '- Begin with the Implementation Plan section',
            '',
            '---',
            '',
            '*This specification was generated using SpecBaker and IBM watsonx.ai*'
        ];

        return lines.join('\n');
    }

    /**
     * Format as code block
     */
    formatCodeBlock(content, language = '') {
        return `\`\`\`${language}\n${content}\n\`\`\``;
    }

    /**
     * Format as table
     */
    formatTable(headers, rows) {
        const lines = [];

        // Header row
        lines.push('| ' + headers.join(' | ') + ' |');

        // Separator row
        lines.push('| ' + headers.map(() => '---').join(' | ') + ' |');

        // Data rows
        rows.forEach(row => {
            lines.push('| ' + row.join(' | ') + ' |');
        });

        return lines.join('\n');
    }

    /**
     * Format as list
     */
    formatList(items, ordered = false) {
        return items.map((item, index) => {
            const prefix = ordered ? `${index + 1}.` : '-';
            return `${prefix} ${item}`;
        }).join('\n');
    }

    /**
     * Format as blockquote
     */
    formatBlockquote(content) {
        return content.split('\n').map(line => `> ${line}`).join('\n');
    }

    /**
     * Add horizontal rule
     */
    addHorizontalRule() {
        return '---';
    }

    /**
     * Format heading
     */
    formatHeading(text, level = 2) {
        const hashes = '#'.repeat(Math.min(level, 6));
        return `${hashes} ${text}`;
    }

    /**
     * Format bold text
     */
    formatBold(text) {
        return `**${text}**`;
    }

    /**
     * Format italic text
     */
    formatItalic(text) {
        return `*${text}*`;
    }

    /**
     * Format inline code
     */
    formatInlineCode(text) {
        return `\`${text}\``;
    }

    /**
     * Format link
     */
    formatLink(text, url) {
        return `[${text}](${url})`;
    }

    /**
     * Validate markdown syntax
     */
    validate(markdown) {
        const errors = [];

        // Check for unclosed code blocks
        const codeBlockCount = (markdown.match(/```/g) || []).length;
        if (codeBlockCount % 2 !== 0) {
            errors.push('Unclosed code block detected');
        }

        // Check for proper heading hierarchy
        const headings = markdown.match(/^#{1,6}\s+.+$/gm) || [];
        let lastLevel = 0;
        headings.forEach(heading => {
            const level = heading.match(/^#+/)[0].length;
            if (level > lastLevel + 1) {
                errors.push(`Heading level skipped: ${heading}`);
            }
            lastLevel = level;
        });

        return {
            valid: errors.length === 0,
            errors
        };
    }

    /**
     * Capitalize first letter
     */
    capitalizeFirst(str) {
        if (!str) return '';
        return str.charAt(0).toUpperCase() + str.slice(1);
    }

    /**
     * Clean and normalize markdown
     */
    normalize(markdown) {
        return markdown
            // Remove excessive blank lines
            .replace(/\n{4,}/g, '\n\n\n')
            // Ensure proper spacing around headings
            .replace(/([^\n])\n(#{1,6}\s)/g, '$1\n\n$2')
            // Ensure proper spacing after headings
            .replace(/^(#{1,6}\s.+)\n([^\n])/g, '$1\n\n$2')
            // Trim trailing whitespace
            .replace(/[ \t]+$/gm, '')
            // Ensure file ends with newline
            .replace(/\n*$/, '\n');
    }

    /**
     * Get word count
     */
    getWordCount(markdown) {
        // Remove code blocks
        const withoutCode = markdown.replace(/```[\s\S]*?```/g, '');
        // Remove markdown syntax
        const plainText = withoutCode
            .replace(/[#*`>\[\]()]/g, '')
            .replace(/\n+/g, ' ');
        // Count words
        return plainText.trim().split(/\s+/).length;
    }

    /**
     * Get reading time estimate (words per minute)
     */
    getReadingTime(markdown, wpm = 200) {
        const wordCount = this.getWordCount(markdown);
        const minutes = Math.ceil(wordCount / wpm);
        return minutes;
    }
}

module.exports = MarkdownFormatter;

